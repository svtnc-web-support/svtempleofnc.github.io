function getTodaysDate() {

    var today = new Date();
    var dd = today. getDate();
    var mm = today. getMonth()+1;
    var yyyy = today. getFullYear();
    var todayDate = dd + '/' + mm + '/' + yyyy;
    return todayDate;
}

